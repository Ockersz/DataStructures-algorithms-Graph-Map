    private void btn_addcityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addcityActionPerformed

        if (txt_cityname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "City Name cannot be blank", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            controller.createVertex(txt_cityname.getText(), Double.parseDouble(txt_longitude.getText()), Double.parseDouble(txt_latitude.getText()));
            refresh();
        }
    }//GEN-LAST:event_btn_addcityActionPerformed

    private void btn_addpathActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addpathActionPerformed
        if (cmb_source.getSelectedIndex() == 0 || cmb_destination.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Please pick both Source and Destination Cities", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (txt_distance.getText().matches("//d+")) {
            JOptionPane.showMessageDialog(null, "Distance can only contain numbers.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            controller.createEdge(cmb_source.getSelectedItem().toString(), cmb_destination.getSelectedItem().toString(), Integer.parseInt(txt_distance.getText()));
            refresh();
        }
    }//GEN-LAST:event_btn_addpathActionPerformed


