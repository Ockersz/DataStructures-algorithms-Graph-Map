    public void refresh() {
        cmb_source.removeAllItems();
        cmb_destination.removeAllItems();
        cmb_source.addItem("--Select--");
        cmb_destination.addItem("--Select--");
        Graph graph = new Graph();
        List<String> vertices = graph.getVertices();
        for (String vertex : vertices) {
            cmb_source.addItem(vertex);
            cmb_destination.addItem(vertex);
        }
    }

